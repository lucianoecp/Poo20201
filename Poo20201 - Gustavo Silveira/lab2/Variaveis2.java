package lab2;

public class Variaveis2 {
    
    public static void main(String[] args) {

        int variavel1=1350, variavel2 = 1300;  //declaro 2 variaveis inteiras e atribuo valor a elas
        System.out.println(variavel1);   //dou print nas 2
        System.out.println(variavel2);
        
        variavel1=variavel2;      //passo o valor de uma para a outra
        System.out.println(variavel1);       //dou print na variavel 1
        
        variavel2=1600;     //dou outro valor a variavel 2
        System.out.println(variavel2);     //dou print nas 2 novamente
        System.out.println(variavel1);
    }
}
   

    