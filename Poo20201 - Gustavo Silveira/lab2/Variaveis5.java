package lab2;

public class Variaveis5 {
    public static void main(String[] args) {  //operadores pre/pro inseridos (--/++)

    int ValorNum = 20;   //declaro o valor como sendo int e recebendo 20 

    System.out.println(++ValorNum);   // 21
    System.out.println(ValorNum);   // 21

    ValorNum= 20;     //reinicio o valor novamente para 20

    System.out.println(ValorNum++);    // 20
    System.out.println(ValorNum);     // 21
  } 
}