public class Variaveis2 {
    
    public static void main(String[] args) {
        int varTeste;
        System.out.println(varTeste);
        
        varTeste = 50;
        System.out.println(varTeste);
        
        int varTeste2 = 100;
        System.out.println(varTeste2);
        
        varTeste = varTeste2;
        System.out.println(varTeste);
        
        varTeste2 = 200;
        System.out.println(varTeste);
        
        System.out.println(varTeste2);
    }
}