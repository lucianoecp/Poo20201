public class Variaveis5 {
  public static void main(String[] args) {
  // operadores pre-fixados/pos-fixados
    int valor = 200;
    System.out.println(++valor);
    System.out.println(++valor);
    System.out.println(++valor);
    System.out.println(++valor); 
    System.out.println(valor); 
    int valor2= 5;
    System.out.println(valor2++); 
    System.out.println(valor + valor2); 
  }
  }