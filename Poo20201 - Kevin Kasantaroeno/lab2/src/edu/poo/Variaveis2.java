package edu.poo;

public class Variaveis2 {
    public static void main(String[] args){
        int variavel = 0;
        System.out.println(variavel);

        variavel = 350;
        System.out.println(variavel);

        int outraVariavel = 500;
        System.out.println(outraVariavel);

        variavel = outraVariavel;
        System.out.println(variavel);

        outraVariavel = 600;
        System.out.println(outraVariavel);

        System.out.println(variavel);
    }
}
