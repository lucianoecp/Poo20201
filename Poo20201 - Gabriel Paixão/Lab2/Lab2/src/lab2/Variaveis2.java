package lab2;

public class Variaveis2 {
    public static void main(String[] args)
    {
        int n1; //declaro duas variaveis
        int n2;

        n1 = 10; // dou valor a elas
        n2 = 20;

        System.out.println(n1 + ", " + n2); // printo seus valores

        n1 = n2; //atribuo o valor da variavel n2 na variavel n1

        System.out.println(n1 + ", " + n2); // printo seus valores

        n2 = 30; //altero o valor de n2

        System.out.println(n1 + ", " + n2); // printo seus valores

    }
}
