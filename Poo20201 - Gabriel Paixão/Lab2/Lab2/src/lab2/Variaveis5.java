package lab2;

public class Variaveis5 {
    public static void main(String[] args)
    {
        int valor = 10; // declaro um valor

        System.out.println(++valor); // valor = 11
        System.out.println(valor); // valor = 11

        valor = 10; // volto ao valor inicial

        System.out.println(valor++); // valor = 10
        System.out.println(valor); // valor = 11
    }
}
