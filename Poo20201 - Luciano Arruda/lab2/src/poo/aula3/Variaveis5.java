package poo.aula3;

public class Variaveis5 {

    public static void main(String[] args) 
    {
        // Operadores pré-fixados vs pós-fixados:
        int valor = 6;
        System.out.println(++valor); //7
        System.out.println(valor); //700

        valor = 6;
        System.out.println(valor++); // 7
        System.out.println(valor);

        
    }
    
}
